package com.example.ligaindonesia;

public class ClubModel {
    private String ClubName;
    private String ClubDetail;
    private int ClubImages;


    public String getClubName() {
        return ClubName;
    }

    public void setClubName(String clubName) { ClubName = clubName;
    }

    public String getClubDetail() {
        return ClubDetail;
    }

    public void setClubDetail(String clubDetail) {
        ClubDetail = clubDetail;
    }

    public int getClubImage() {
        return ClubImages;
    }

    public void setClubImage(int clubImage) {
        ClubImages = clubImage;
    }
}
